/// Evaluator for Swish expressions
public class Evaluator {
    public init() {}

    /// Evaluates a Swish expression
    public func eval(_ expr: Expr) -> Expr {
        switch expr {
        case .integer:
            expr

        case .float:
            expr

        case .ratio:
            expr

        case .string:
            expr

        case .character:
            expr

        case .boolean:
            expr
        }
    }
}
